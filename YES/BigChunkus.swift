import Foundation


struct Chunk {
    var sender: String
    var name: String
    var index: Int
    var total: Int
    var chunk: Data
    
}

struct FileMessage {
    var sender: String
    var name: String
    var file: Data
    
}

enum BigChunkusError: Error {
    case fileIsEmpty
    case DataInconsistency
    case unmachingChunkSize
}

class BigChunkus {
    let chunkSize = 1024 * 1024
    var chunks: [Chunk] = []
    
    func clear() {
        self.chunks = []
    }
    
    func addChunk(chunk: Chunk) throws -> Bool {
        if (chunk.chunk.count != chunkSize && chunk.index != chunk.total ) {throw BigChunkusError.unmachingChunkSize}
        if (chunks.isEmpty) {
            if (chunk.index != 0) {throw BigChunkusError.DataInconsistency}
            chunks.append(chunk)
        } else {
            if chunk.name == chunks.last?.name,
               chunk.index + 1 == chunks.last?.index,
               chunk.total == chunks.last?.total
            {
                chunks.append(chunk)
            } else {
                throw BigChunkusError.DataInconsistency
            }
        }
        return chunks.count == chunk.total
    }
    
    static func chunkerize(file: Data, fileName: String, sender: String, chunkSize: Int = 1024 * 1024) throws -> [Chunk] {
        var chunks: [Chunk] = []
        if (file.isEmpty) {throw BigChunkusError.fileIsEmpty} // TODO: replace with exception
        var offset = 0
        var index = 0
        let total = ceil(Double(file.count/chunkSize))
        
        while (offset < file.count - 1){
            var newOffset = offset+chunkSize
            if (newOffset > file.count) {newOffset = file.count - 1}
            chunks.append(Chunk(
                sender: sender,
                name: fileName,
                index: index,
                total: Int(total),
                chunk: file.subdata(in: Range(offset...newOffset)))
            )
            offset = newOffset
            index+=1
        }
        
        return chunks
    }
    
    func unChunkerize() throws -> FileMessage {
        if (chunks.isEmpty || chunks.last?.index != chunks.last?.total) {throw BigChunkusError.DataInconsistency}
        var data = Data()
        for  chunk in chunks {
            data.append(chunk.chunk)
        }
        let fileMessage = FileMessage(
            sender: chunks.last!.sender,
            name: chunks.last!.name,
            file: data
        )
        return fileMessage
    }
}
